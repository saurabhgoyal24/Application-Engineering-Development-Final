public class DrugGuideline {
    
    private String drugInfo;
    private String drugUse;
    private String drugNoUse;
    private String drugDoctorInfo;
    private String howToUse;
    private String sideEffect;
    private String storeInfo;
    private String ingridients;
    private String refillInfo;

    public String getDrugInfo() {
        return drugInfo;
    }

    public void setDrugInfo(String drugInfo) {
        this.drugInfo = drugInfo;
    }

    public String getDrugUse() {
        return drugUse;
    }

    public void setDrugUse(String drugUse) {
        this.drugUse = drugUse;
    }

    public String getDrugNoUse() {
        return drugNoUse;
    }

    public void setDrugNoUse(String drugNoUse) {
        this.drugNoUse = drugNoUse;
    }

    public String getDrugDoctorInfo() {
        return drugDoctorInfo;
    }

    public void setDrugDoctorInfo(String drugDoctorInfo) {
        this.drugDoctorInfo = drugDoctorInfo;
    }

    public String getHowToUse() {
        return howToUse;
    }

    public void setHowToUse(String howToUse) {
        this.howToUse = howToUse;
    }

    public String getSideEffect() {
        return sideEffect;
    }

    public void setSideEffect(String sideEffect) {
        this.sideEffect = sideEffect;
    }

    public String getStoreInfo() {
        return storeInfo;
    }

    public void setStoreInfo(String storeInfo) {
        this.storeInfo = storeInfo;
    }

    public String getIngridients() {
        return ingridients;
    }

    public void setIngridients(String ingridients) {
        this.ingridients = ingridients;
    }


    public String getRefillInfo() {
        return refillInfo;
    }

    public void setRefillInfo(String refillInfo) {
        this.refillInfo = refillInfo;
    }
    
    
}
