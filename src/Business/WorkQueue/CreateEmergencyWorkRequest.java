package Business.WorkQueue;

import Business.Enterprise.Enterprise;
import Business.Person.Doctor;

/**
 *
 * @author Saurabh Goyal
 */
public class CreateEmergencyWorkRequest extends WorkRequest {
    private long medicalRecordNumber;
    private String patientName;
    private Doctor doctor;
    private Enterprise senderEnterprise;

    public Enterprise getSenderEnterprise() {
        return senderEnterprise;
    }

    public void setSenderEnterprise(Enterprise senderEnterprise) {
        this.senderEnterprise = senderEnterprise;
    }
    
    

    public long getMedicalRecordNumber() {
        return medicalRecordNumber;
    }

    public void setMedicalRecordNumber(long medicalRecordNumber) {
        this.medicalRecordNumber = medicalRecordNumber;
    }

    public String getPatientName() {
        return patientName;
    }

    public void setPatientName(String patientName) {
        this.patientName = patientName;
    }

    public Doctor getDoctor() {
        return doctor;
    }

    public void setDoctor(Doctor doctor) {
        this.doctor = doctor;
    }

    @Override
    public String toString() {
        return super.getMessage();
    }
    
    
}
