    private void populateDrugTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblDrugs.getModel();
        dtm.setRowCount(0);
        
        for(Drug d : pharmaceuticalCompanyEnterprise.getDrugList()){
            if(d.getIsApproved().equals("Approved")){
                Object[] row = new Object[3];
                
                row[0] = d;
                row[1] = d.getDrugName();
                row[2] = d.getIsApproved();
                
                dtm.addRow(row);
            }
        }
    }

