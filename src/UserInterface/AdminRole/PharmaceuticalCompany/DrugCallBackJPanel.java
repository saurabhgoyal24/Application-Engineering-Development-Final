    private void btnSendRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendRequestActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDrugs.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a drug first.");
            return;
        }
        
        String message  = txtMessage.getText();
        if(message.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter a message first.");
            return;
        }
        Drug drug = (Drug)tblDrugs.getValueAt(selectedRow,0);
        
        
        CallBackWorkRequest cbwr = new CallBackWorkRequest();
        cbwr.setMessage(message);
        cbwr.setSenderEnterprise(pharmaceuticalCompanyEnterprise);
        cbwr.setDrug(drug);
        
        drug.setIsApproved("Called Back");
        for(PharmacyEnterprise pe : pharmaceuticalCompanyEnterprise.getPharmacyEnterpriseList()){
            pe.getWorkQueue().getWorkRequestList().add(cbwr);
        }
        pharmaceuticalCompanyEnterprise.getWorkQueue().getWorkRequestList().add(cbwr);
        btnSendRequest.setEnabled(false);
    }//GEN-LAST:event_btnSendRequestActionPerformed


