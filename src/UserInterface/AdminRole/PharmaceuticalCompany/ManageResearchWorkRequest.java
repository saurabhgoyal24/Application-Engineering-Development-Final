    private void btnSendRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendRequestActionPerformed
        // TODO add your handling code here:
        String message = txtMessage.getText();
        Drug drug = (Drug) cmbDrug.getSelectedItem();

        if (message.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter a message first.");
            return;
        }

        if (drug == null) {
            JOptionPane.showMessageDialog(null, "There is no drug manufactured yet.");
            return;
        }

        ResearchOrganizationWorkRequest rowr = new ResearchOrganizationWorkRequest();
        rowr.setMessage(message);
        rowr.setDrug(drug);
        rowr.setStatus("Sent to RO");

        ro.getWorkQueue().getWorkRequestList().add(rowr);
    }//GEN-LAST:event_btnSendRequestActionPerformed

