    private void populateDrugComboBox() {
        cmbDrug.removeAllItems();

        for (Drug d : pce.getDrugList()) {
            cmbDrug.addItem(d);
        }
        populateResearchWorkRequestTable();
    }

    private void populateResearchWorkRequestTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblResearchWorkRequest.getModel();
        dtm.setRowCount(0);

        for (WorkRequest wr : ro.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof ResearchOrganizationWorkRequest) {
                ResearchOrganizationWorkRequest rowr = (ResearchOrganizationWorkRequest) wr;

                Object[] row = new Object[6];
                row[0] = rowr;
                row[1] = rowr.getDrug().getDrugId();
                row[2] = rowr.getDrug().getDrugName();
                if (rowr.getReceiver() != null) {
                    row[3] = rowr.getReceiver().getFirstName() + " " + rowr.getReceiver().getLastName();
                }
                row[4] = rowr.getStatus();
                row[5] = rowr.getStatus();

                dtm.addRow(row);
            }
        }
    }

