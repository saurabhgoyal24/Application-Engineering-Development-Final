    private void populateCompanyTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblCompany.getModel();
        dtm.setRowCount(0);

        for (CountryNetwork cn : system.getCountryNetworkList()) {
            for (StateNetwork sn : cn.getStateNetwork()) {
                for (CityNetwork cin : sn.getCityNetwork()) {
                    for (Enterprise e : cin.getEnterpriseDirectory().getEnterpriseList()) {
                        if (e instanceof PharmaceuticalCompanyEnterprise) {

                            Object[] row = new Object[2];

                            row[0] = e.getOrgId();
                            row[1] = e;

                            dtm.addRow(row);
                        }
                    }
                }
            }
        }
    }

    private void populateDrugItemTable(Enterprise e) {

        DefaultTableModel dtm = (DefaultTableModel) tblDrugItem.getModel();
        dtm.setRowCount(0);
        PharmaceuticalCompanyEnterprise pce = (PharmaceuticalCompanyEnterprise) e;
        for (DrugItem di : pce.getDrugItemList()) {
            if ("Approved".equals(di.getDrug().getIsApproved())) {
                
                Object[] row = new Object[5];

                row[0] = di;
                row[1] = di.getDrug().getDrugName();
                row[2] = di.getDateOfManufacturing();
                row[3] = di.getDateOfExpiry();
                row[4] = di.getQuantity();

                dtm.addRow(row);
            }
        }
    }

