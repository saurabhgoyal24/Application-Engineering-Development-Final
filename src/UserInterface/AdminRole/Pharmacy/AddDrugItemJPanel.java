    private void btnAddDrugItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddDrugItemActionPerformed
        // TODO add your handling code here:
        int quantity = (Integer) spnQuantity.getValue();
        int selectedRow = tblDrugItem.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a Drug Item first.");
            return;
        }
        DrugItem di = (DrugItem) tblDrugItem.getValueAt(selectedRow, 0);

        if (quantity < 1 || quantity > di.getQuantity()) {
            JOptionPane.showMessageDialog(null, "Quantity entered is invalid.");
            return;
        }

        DrugItem di1 = new DrugItem();

        di1.setDateOfExpiry(di.getDateOfExpiry());
        di1.setDateOfManufacturing(di.getDateOfManufacturing());
        di1.setQuantity(quantity);
        di1.setDrug(di.getDrug());

        di.setQuantity(di.getQuantity() - quantity);

        PharmacyEnterprise pe = (PharmacyEnterprise) enterprise;
        pe.getDrugItemList().add(di1);

        for (PharmacyEnterprise pe1 : pce.getPharmacyEnterpriseList()) {
            if (pe == pe1) {
                return;
            }
        }
        pce.getPharmacyEnterpriseList().add(pe);

    }//GEN-LAST:event_btnAddDrugItemActionPerformed


