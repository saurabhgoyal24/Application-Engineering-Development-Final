    private void populateDrugItemList() {
        DefaultTableModel dtm = (DefaultTableModel) tblDrugItem.getModel();
        dtm.setRowCount(0);

        PharmacyEnterprise pe = (PharmacyEnterprise) enterprise;

        for (DrugItem di : pe.getDrugItemList()) {

            if (di.getDrug() != null) {
                Object[] row = new Object[5];
                row[0] = di;
                row[1] = di.getDrug().getDrugName();
                row[2] = di.getQuantity();
                row[3] = di.getDateOfManufacturing();
                row[4] = di.getDateOfExpiry();

                dtm.addRow(row);
            }
        }
    }

