    private void btnViewDrugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDrugActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDrugItem.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a Drug first.");
            return;
        }
        DrugItem di = (DrugItem) tblDrugItem.getValueAt(selectedRow, 0);

        ViewDrugDetailJPanel vddjp = new ViewDrugDetailJPanel(userProcessContainer, di.getDrug());
        userProcessContainer.add("View Drug", vddjp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnViewDrugActionPerformed

    private void btnRemoveDrugItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveDrugItemActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDrugItem.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a Drug first.");
            return;
        }
        int c = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this drug.", "Warning", JOptionPane.YES_NO_OPTION);
        if (c == JOptionPane.YES_OPTION) {
            DrugItem di = (DrugItem) tblDrugItem.getValueAt(selectedRow, 0);
            PharmacyEnterprise pe = (PharmacyEnterprise) enterprise;
            pe.getDrugItemList().remove(di);
            populateDrugItemList();
        }
    }//GEN-LAST:event_btnRemoveDrugItemActionPerformed

