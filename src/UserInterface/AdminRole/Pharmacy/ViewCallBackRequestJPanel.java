    private void populateRequestTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblRequests.getModel();
        dtm.setRowCount(0);
        
        for(WorkRequest wr : enterprise.getWorkQueue().getWorkRequestList()){
            if(wr instanceof CallBackWorkRequest){
                CallBackWorkRequest cbwr = (CallBackWorkRequest)wr;
                
                Object[] row = new Object[4];
                
                row[0] = cbwr;
                row[1] = cbwr.getDrug();
                row[2] = cbwr.getDrug().getDrugName();
                row[3] = cbwr.getSenderEnterprise();
                
                dtm.addRow(row);
            }
        }
    }

