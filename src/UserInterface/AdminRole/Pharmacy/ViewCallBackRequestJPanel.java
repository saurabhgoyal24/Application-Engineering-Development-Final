    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
        boolean flag = false;
        int selectedRow = tblRequests.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select a request to process.");
            return;
        }
        CallBackWorkRequest cbwr = (CallBackWorkRequest)tblRequests.getValueAt(selectedRow, 0);
        PharmacyEnterprise pe = (PharmacyEnterprise)enterprise;
        
        
        
        for(DrugItem di : pe.getDrugItemList()){
            if((di.getDrug()==cbwr.getDrug())){
                di.setDrug(null);
                flag =  true;
            }
        }
        
        if(flag == false){
            JOptionPane.showMessageDialog(null, "Drug not found");
        }
    }//GEN-LAST:event_btnProcessActionPerformed


