    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ArrayList<PharmaceuticalCompanyEnterprise> pharmaCompanyList = new ArrayList<>();
        
        for(CountryNetwork cn : system.getCountryNetworkList()){
            for(StateNetwork sn : cn.getStateNetwork()){
                for(CityNetwork cin : sn.getCityNetwork()){
                    for(Enterprise e : cin.getEnterpriseDirectory().getEnterpriseList()){
                        if(e instanceof PharmaceuticalCompanyEnterprise){
                            PharmaceuticalCompanyEnterprise pce = (PharmaceuticalCompanyEnterprise)e;
                            pharmaCompanyList.add(pce);
                        }
                    }
                }
            }
        }
        ViewDrugGuidelinesJPanel vdgjp = new ViewDrugGuidelinesJPanel(userProcessContainer, pharmaCompanyList);
        userProcessContainer.add("View Drug Guidelines",vdgjp);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_jButton1ActionPerformed

