    private void populateEmergencyRequestTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblEmergencyRequest.getModel();
        dtm.setRowCount(0);
        
        for(WorkRequest wr : person.getWorkQueue().getWorkRequestList()){
            if(wr instanceof CreateEmergencyWorkRequest){
                CreateEmergencyWorkRequest cewr = (CreateEmergencyWorkRequest)wr;
                
                Object[] row = new Object[4];
                
                row[0] = cewr;
                row[1] = cewr.getMedicalRecordNumber();
                row[2] = cewr.getPatientName();
                row[3] = cewr.getSenderEnterprise();
                
                dtm.addRow(row);
            }
        }
    }

