    private void btnIssueEnquiryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIssueEnquiryActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblEmergencyRequest.getSelectedRow();
        
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select an Emergency Request that you want to process.");
            return;
        }
        WorkRequest request = (WorkRequest)tblEmergencyRequest.getValueAt(selectedRow, 0);
        
        Long medicalRecordNumber = (Long)tblEmergencyRequest.getValueAt(selectedRow, 1);
        String message = txtMessage.getText();
        Enterprise enterprise = (Enterprise)tblEmergencyRequest.getValueAt(selectedRow, 3);
        
        EmergencyEnquiryWorkRequest eewr = new EmergencyEnquiryWorkRequest();
        eewr.setMessage(message);
        eewr.setMedicalRecordNumber(medicalRecordNumber);
        eewr.setDoctor((Doctor)person);
        eewr.setEnterprise(enterprise);
        
        for(CountryNetwork cn : system.getCountryNetworkList()){
            for(StateNetwork sn : cn.getStateNetwork()){
                for(CityNetwork cin : sn.getCityNetwork()){
                    for(Enterprise e : cin.getEnterpriseDirectory().getEnterpriseList()){
                        if((e instanceof HospitalEnterprise)||(e instanceof ClinicEnterprise)){
                            e.getWorkQueue().getWorkRequestList().add(eewr);
                        }
                    }
                }
            }
        }
        person.getWorkQueue().getWorkRequestList().add(eewr);
        person.getWorkQueue().getWorkRequestList().remove(request);
        JOptionPane.showMessageDialog(null, "Enquiry issued.");
        populateEmergencyRequestTable();
    }//GEN-LAST:event_btnIssueEnquiryActionPerformed

