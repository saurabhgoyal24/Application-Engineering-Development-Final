    private void btnSendRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendRequestActionPerformed
        // TODO add your handling code here:
        String message = txtMessage.getText();
        if (message.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter message first.");
            return;
        }

        NurseWorkRequest nwr = new NurseWorkRequest();
        nwr.setMessage(message);
        nwr.setSender(doctor);
        nwr.setEnterprise(enterprise);
        nwr.setPatient(patient);
        nwr.setStatus("Sent to Nurse Organization.");

        for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (o instanceof NurseOrganization) {
                o.getWorkQueue().getWorkRequestList().add(nwr);
            }
        }
        doctor.getWorkQueue().getWorkRequestList().add(nwr);


    }//GEN-LAST:event_btnSendRequestActionPerformed


