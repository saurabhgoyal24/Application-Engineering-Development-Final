    private void btnUpdateMedicineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateMedicineActionPerformed
        // TODO add your handling code here:
        Enterprise e = (Enterprise) cmbPharmacy.getSelectedItem();
        if (e == null) {
            JOptionPane.showMessageDialog(null, "Select a pharmacy first");
            return;
        }
        String message = txtMessage.getText();

        if (message.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter a message that you want to update.");
            return;
        }
        PharmacyWorkRequest pwr = new PharmacyWorkRequest();
        Drug d = (Drug) cmbDrugs.getSelectedItem();
        pwr.setSender(encounter.getDoctor());
        pwr.setMessage(message);
        pwr.setEnterprise(encounter.getLabTestWorkRequest().getEnterprise());
        pwr.setPatient(encounter.getPatient());
        pwr.setStatus("Sent to pharmacy");
        pwr.setPharmacyName(e.getOrgName());
        pwr.setDrug(d);
        
        boolean f = false;
        for(Drug dr : encounter.getDrugList()){
            if(dr==d){
                f= true;
            }
        }
        if(f==false){
            encounter.getDrugList().add(d);
        }

        e.getWorkQueue().getWorkRequestList().add(pwr);
        encounter.getPatient().getWorkQueue().getWorkRequestList().add(pwr);
        encounter.getDoctor().getWorkQueue().getWorkRequestList().add(pwr);
        btnUpdateMedicine.setEnabled(false);


    }//GEN-LAST:event_btnUpdateMedicineActionPerformed

