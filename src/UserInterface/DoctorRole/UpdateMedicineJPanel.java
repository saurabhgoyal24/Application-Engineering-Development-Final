    private void cmbPharmacyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbPharmacyActionPerformed
        // TODO add your handling code here:
        Enterprise e = (Enterprise) cmbPharmacy.getSelectedItem();
        if (e != null) {
            lblId.setText(String.valueOf(e.getOrgId()));

            PharmacyEnterprise pe = (PharmacyEnterprise) e;

            ArrayList<Drug> drugList = new ArrayList<Drug>();

            for (DrugItem di : pe.getDrugItemList()) {
                boolean flag = false;
                if (di.getDrug() != null) {
                    Drug d = di.getDrug();

                    for (Drug dr : drugList) {
                        if (d == dr) {
                            flag = true;
                        }

                    }
                    if (flag == false) {
                        drugList.add(d);
                    }
                }
            }
            cmbDrugs.removeAllItems();

            for (Drug d : drugList) {
                cmbDrugs.addItem(d);
            }
        }
    }//GEN-LAST:event_cmbPharmacyActionPerformed

