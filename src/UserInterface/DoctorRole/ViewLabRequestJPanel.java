    private void cmbEnterpriseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbEnterpriseActionPerformed
        // TODO add your handling code here:
        Enterprise e = (Enterprise) cmbEnterprise.getSelectedItem();

        if (e != null) {
            lblId.setText(String.valueOf(e.getOrgId()));

            if (e instanceof HospitalEnterprise) {

                HospitalEnterprise he = (HospitalEnterprise) e;

                DefaultTableModel dtm = (DefaultTableModel) tblWorkRequest.getModel();
                dtm.setRowCount(0);

                for (Encounter encounter : he.getEncounterList()) {
                    if (encounter.getLabTestWorkRequest() != null) {
                        if (encounter.getDoctor() == doctor) {
                            LabTestWorkRequest ltwr = encounter.getLabTestWorkRequest();
                            if ((ltwr.getPatient().getPreferredDoctor() == doctor) || (ltwr.getPatient().getTemproryDoctor() == doctor)) {

                                Object[] row = new Object[6];
                                row[0] = encounter;
                                row[1] = ltwr;
                                row[2] = ltwr.getPatient().getMedicalRecordNumber();
                                if (ltwr.getReceiver() != null) {
                                    row[3] = ltwr.getReceiver().getFirstName() + " " + ltwr.getReceiver().getLastName();
                                }
                                row[4] = ltwr.getStatus();
                                row[5] = ltwr.getLabTestResult();

                                dtm.addRow(row);
                            }
                        }
                    }
                }
            }

            if (e instanceof ClinicEnterprise) {
                ClinicEnterprise ce = (ClinicEnterprise) e;
                DefaultTableModel dtm = (DefaultTableModel) tblWorkRequest.getModel();
                dtm.setRowCount(0);

                for (Encounter encounter : ce.getEncounterList()) {

                    if (encounter.getDoctor() == doctor) {

                        if (encounter.getLabTestWorkRequest() != null) {

                            LabTestWorkRequest ltwr = encounter.getLabTestWorkRequest();
                            if ((ltwr.getPatient().getPreferredDoctor() == doctor) || (ltwr.getPatient().getTemproryDoctor() == doctor)) {

                                Object[] row = new Object[6];
                                row[0] = encounter;
                                row[1] = ltwr;
                                row[2] = ltwr.getPatient().getMedicalRecordNumber();
                                if (ltwr.getReceiver() != null) {
                                    row[3] = ltwr.getReceiver().getFirstName() + " " + ltwr.getReceiver().getLastName();
                                }
                                row[4] = ltwr.getStatus();
                                row[5] = ltwr.getLabTestResult();

                                dtm.addRow(row);
                            }

                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_cmbEnterpriseActionPerformed

