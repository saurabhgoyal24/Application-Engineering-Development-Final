    private void btnUpdateMedicineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateMedicineActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblWorkRequest.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select a request first.");
            return;
        }
        Encounter encounter = (Encounter) tblWorkRequest.getValueAt(selectedRow, 0);

        UpdateMedicineJPanel umjp = new UpdateMedicineJPanel(userProcessContainer, system, encounter);
        userProcessContainer.add("Update Medicine", umjp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnUpdateMedicineActionPerformed


