    private void btnAddDrugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddDrugActionPerformed
        // TODO add your handling code here:
        String drugInfo = txtDrugInfo.getText();
        String drugUse = txtDrugUse.getText();
        String drugNoUse = txtDrugNoUse.getText();
        String drugDoctorInfo = txtDurgDoctorInfo.getText();
        String howToUse = txtHowToUse.getText();
        String sideEffect = txtSideEffect.getText();
        String storeinfo = txtStoreInfo.getText();
        String ingridients = txtIngridients.getText();
        String refillInfo = txtRefillInfo.getText();
        
        if(drugInfo.isEmpty()||drugUse.isEmpty()||drugNoUse.isEmpty()||drugDoctorInfo.isEmpty()||howToUse.isEmpty()
                ||sideEffect.isEmpty()||storeinfo.isEmpty()||ingridients.isEmpty()||refillInfo.isEmpty()){
            JOptionPane.showMessageDialog(null, "Incomplete information is entered.");
            return;
        }
        DrugGuideline guideline = drug.getDrugGuideline();
        guideline.setDrugDoctorInfo(drugDoctorInfo);
        guideline.setDrugInfo(drugInfo);
        guideline.setDrugNoUse(drugNoUse);
        guideline.setDrugUse(drugUse);
        guideline.setHowToUse(howToUse);
        guideline.setIngridients(ingridients);
        guideline.setRefillInfo(refillInfo);
        guideline.setSideEffect(sideEffect);
        guideline.setStoreInfo(storeinfo);
        
        btnAddDrug.setEnabled(false);
        pce.getDrugList().add(drug);
    }//GEN-LAST:event_btnAddDrugActionPerformed


