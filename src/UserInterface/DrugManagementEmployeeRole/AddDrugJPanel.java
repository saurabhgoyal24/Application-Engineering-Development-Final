    private void btnAddDrugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddDrugActionPerformed
        // TODO add your handling code here:

        if (txtDrugName.getText().isEmpty() 
                || txtDrugStrength.getText().isEmpty() || txtPrice.getText().isEmpty()) {
            
            JOptionPane.showMessageDialog(null,"Incomplete information is filled.");
            return;

        }
        try {
            String drugName = txtDrugName.getText();
            float drugStrength = Float.parseFloat(txtDrugStrength.getText());
            float price = Float.parseFloat(txtPrice.getText());

            Drug drug = new Drug();

            drug.setDrugName(drugName);
            drug.setDrugStrength(drugStrength);
            drug.setPrice(price);
            drug.setIsApproved("No");
            drug.setPharmaceuticalCompanyEnterprise(pce);
            
            
            txtDrugName.getText().isEmpty();
            txtDrugStrength.getText().isEmpty();
            txtPrice.getText().isEmpty();

            AddDrugGuidelineJPanel adgjp = new AddDrugGuidelineJPanel(userProcessContainer, pce, drug);
            userProcessContainer.add("Add drug guideline",adgjp);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
            
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid information is entered.");
        }

    }//GEN-LAST:event_btnAddDrugActionPerformed


