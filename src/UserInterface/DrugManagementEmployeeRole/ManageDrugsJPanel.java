    private void populatedrugTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblDrugs.getModel();
        dtm.setRowCount(0);
        
        for (Drug d : pce.getDrugList()) {
            
            Object[] row = new Object[4];
            
            row[0] = d;
            row[1] = d.getDrugName();
            row[2] = d.getDrugStrength();
            row[3] = d.getIsApproved();
            
            dtm.addRow(row);
            
        }
    }

