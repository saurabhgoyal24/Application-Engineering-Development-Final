    private void btnRemoveDrugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveDrugActionPerformed
        // TODO add your handling code here:

        int selectedRow = tblDrugs.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a drug first.");
            return;
        }
        
        int c = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete.", "Warning", JOptionPane.YES_NO_OPTION);
        if (c == JOptionPane.YES_OPTION) {
            
            Drug d = (Drug) tblDrugs.getValueAt(selectedRow, 0);
            
            for (DrugItem di : pce.getDrugItemList()) {
                if (di.getDrug() == d) {
                    pce.getDrugItemList().remove(di);
                }
            }
            pce.getDrugList().remove(d);
            
            populatedrugTable();
            
        }
    }//GEN-LAST:event_btnRemoveDrugActionPerformed

    private void btnSendApprovalRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendApprovalRequestActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDrugs.getSelectedRow();
        
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a drug first.");
            return;
        }
        Drug drug = (Drug) tblDrugs.getValueAt(selectedRow, 0);
        
        if (drug.getIsApproved().equals("Yes")) {
            JOptionPane.showMessageDialog(null, "Drug has already been approved.");
            return;
        }
        
        SendApprovalRequestJPanel sarjp = new SendApprovalRequestJPanel(userProcessContainer, pce, mainAuthorityEnterprise, drug);
        userProcessContainer.add("Send Approval Request", sarjp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
    }//GEN-LAST:event_btnSendApprovalRequestActionPerformed

