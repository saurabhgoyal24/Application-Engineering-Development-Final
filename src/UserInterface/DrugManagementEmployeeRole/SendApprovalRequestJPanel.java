        dmewr.setMessage(txtMessage.getText());
        dmewr.setStatus("Sent");
        dmewr.setSenderEnterpriseID(enterprise.getOrgId());
        dmewr.setSenderEnterpriseName(enterprise.getOrgName());
        dmewr.setDrug(drug);
        
        enterprise.getWorkQueue().getWorkRequestList().add(dmewr);
        mainAuthorityEnterprise.getWorkQueue().getWorkRequestList().add(dmewr);
        btnSendrequest.setEnabled(false);
