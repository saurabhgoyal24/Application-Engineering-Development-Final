    private void btnViewPatientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewPatientActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblPatients.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Select a patient first.");
            return;
        }
        UserAccount ua = (UserAccount)tblPatients.getValueAt(selectedRow, 1);
        Person p = ua.getPerson();
        
        ViewPatientJPanel vpjp = new ViewPatientJPanel(userProcessContainer, p);
        userProcessContainer.add("View Patient",vpjp);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnViewPatientActionPerformed


