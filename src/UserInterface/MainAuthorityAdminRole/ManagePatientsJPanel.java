    private void populatePatientTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblPatients.getModel();
        dtm.setRowCount(0);

        for(UserAccount ua : patientOrganization.getUserAccountDirectory().getUserAccountList()){
            Patient p = (Patient)ua.getPerson();
            
            Object[] row = new Object[4];
            row[0] = p.getMedicalRecordNumber();
            row[1] = ua;
            row[2] = p.getFirstName() + " " + p.getLastName();
            row[3] = p.getAllergies();
            
            dtm.addRow(row);
        }
    }

