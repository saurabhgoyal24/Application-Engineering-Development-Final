    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDiseases.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a disease first.");
            return;
        }
        Disease disease = (Disease)tblDiseases.getValueAt(selectedRow,0);
        ThreadsJPanel tjp = new ThreadsJPanel(userProcessContainer, dmo, disease, person);
        userProcessContainer.add("Threads",tjp);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnNextActionPerformed


