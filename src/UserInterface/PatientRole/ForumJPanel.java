    private void populateDiseaseTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblDiseases.getModel();
        dtm.setRowCount(0);
        
        for(Disease d:dmo.getDiseaseList()){
            Object[] row = new Object[4];
            row[0] = d;
            row[1] = d.getIcdCode();
            row[2] = d.getSymptoms();
            row[3] = d.getDescription();
            
            dtm.addRow(row);
        }
    }

