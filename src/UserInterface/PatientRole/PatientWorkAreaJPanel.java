    private void btnForumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnForumActionPerformed
        // TODO add your handling code here:

        for (Organization o : system.getMainAuthorityEnterprise().getOrganizationDirectory().getOrganizationList()) {
            if (o instanceof DiseaseManagementOrganization) {
                DiseaseManagementOrganization dmo = (DiseaseManagementOrganization) o;
                ForumJPanel fjp = new ForumJPanel(userProcessContainer, dmo, patient);
                userProcessContainer.add("Forum", fjp);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
        }

    }//GEN-LAST:event_btnForumActionPerformed

