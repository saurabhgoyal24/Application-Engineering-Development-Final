    private void populateThreadTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblThreads.getModel();
        dtm.setRowCount(0);

        for (Topic t : dmo.getTopicList()) {
            if (t.getDisease() == disease) {
                
                Object[] row = new Object[2];
                row[0] = t;
                row[1] = t.getWorkQueue().getWorkRequestList().size();

                dtm.addRow(row);
            }
        }
    }

