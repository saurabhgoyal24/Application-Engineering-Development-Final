    private void btnCreateTopic1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateTopic1ActionPerformed
        // TODO add your handling code here:
        String topicName = txtTopicName.getText();
        if(topicName.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter a topic name first.");
            return;
        }
        
        for(Topic t : dmo.getTopicList()){
            if(t.getTopicName() == topicName && t.getDisease()==disease){
                JOptionPane.showMessageDialog(null,"This topic name already exist for this disease.");
                return;
            }
        }
        
        Topic topic = new Topic();
        topic.setDisease(disease);
        topic.setThreadName(topicName);
        dmo.getTopicList().add(topic);
        
    }//GEN-LAST:event_btnCreateTopic1ActionPerformed

