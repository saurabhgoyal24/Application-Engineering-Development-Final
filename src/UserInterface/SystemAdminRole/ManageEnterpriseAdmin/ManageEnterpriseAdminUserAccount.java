    private void btnDeleteEnterpriseAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteEnterpriseAdminActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAdminUserAccounts.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select an admin first.");
            return;
        }
        
        Enterprise e = (Enterprise)tblAdminUserAccounts.getValueAt(selectedRow, 0);
        UserAccount ua = (UserAccount)tblAdminUserAccounts.getValueAt(selectedRow,2);
        
        int d = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete.", "Warning", JOptionPane.YES_NO_OPTION);
        if(d==JOptionPane.YES_OPTION){
            e.getPersonDirectory().getPerosonList().remove(ua.getPerson());
            e.getUserAccountDirectory().getUserAccountList().remove(ua);
        }
    }//GEN-LAST:event_btnDeleteEnterpriseAdminActionPerformed


