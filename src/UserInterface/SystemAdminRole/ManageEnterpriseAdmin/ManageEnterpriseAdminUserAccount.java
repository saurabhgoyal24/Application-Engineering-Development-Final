    private void populateAdminUserAccountTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblAdminUserAccounts.getModel();
        dtm.setRowCount(0);

        if(system.getMainAuthorityEnterprise()!=null){
            for(UserAccount ua : system.getMainAuthorityEnterprise().getUserAccountDirectory().getUserAccountList()){
                if(ua.getRole() instanceof MainAuthorityAdminRole){
                    Object[] row = new Object[6];
                                
                                row[0] = system.getMainAuthorityEnterprise();
                                row[1] = system.getMainAuthorityEnterprise().getEnterpriseType();
                                row[2] = ua;
                                                               
                                dtm.addRow(row);
                }
            }
        }
        for (CountryNetwork cn : system.getCountryNetworkList()) {
            for (StateNetwork sn : cn.getStateNetwork()) {
                for (CityNetwork cin : sn.getCityNetwork()) {
                    for (Enterprise e : cin.getEnterpriseDirectory().getEnterpriseList()) {
                        for (UserAccount ua : e.getUserAccountDirectory().getUserAccountList()) {
                            if (ua.getRole() instanceof AdminRole) {
                                
                                Object[] row = new Object[6];
                                
                                row[0] = e;
                                row[1] = e.getEnterpriseType();
                                row[2] = ua;
                                row[3] = cn;
                                row[4] = sn;
                                row[5] = cin;
                                
                                dtm.addRow(row);
                            }
                        }
                    }
                }
            }
        }
    }

