    private void btnRemoveCountryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveCountryActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCountry.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a country first.");
            return;
        }

        CountryNetwork cn = (CountryNetwork) tblCountry.getValueAt(selectedRow, 0);
        int c = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this city.", "Warning", JOptionPane.YES_NO_OPTION);
        if (c == JOptionPane.YES_OPTION) {
            system.getCountryNetworkList().remove(cn);
            populateCountryTable();
        }
    }//GEN-LAST:event_btnRemoveCountryActionPerformed


