    private void btnAddCountryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCountryActionPerformed
        // TODO add your handling code here:
        
        String coName = txtCountryName.getText();
        Pattern pattern = Pattern.compile("[a-zA-Z ]+");
        Matcher countryName = pattern.matcher(coName);
        
        if(!countryName.matches()){
            JOptionPane.showMessageDialog(null, "Invalid country name.");
            return;
        }
        if (coName.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter the country you want to add.");
            return;
        }

        for (CountryNetwork cn : system.getCountryNetworkList()) {
            if (cn.getCountryName().equals(coName)) {
                JOptionPane.showMessageDialog(null, "This country name already exists.");
                return;
            }
        }
        CountryNetwork cn = system.createCountryNetwork();
        cn.setCountryName(coName);
        populateCountryTable();
    }//GEN-LAST:event_btnAddCountryActionPerformed

