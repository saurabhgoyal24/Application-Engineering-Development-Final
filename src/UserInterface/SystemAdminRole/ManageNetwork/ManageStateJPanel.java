    private void btnRemoveStateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveStateActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblState.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a State first.");
            return;
        }

        StateNetwork sn = (StateNetwork) tblState.getValueAt(selectedRow, 0);
        int c = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this state.", "Warning", JOptionPane.YES_NO_OPTION);
        if (c == JOptionPane.YES_OPTION) {
            cn.getStateNetwork().remove(sn);
            populateStateTable(cn);
        }
    }//GEN-LAST:event_btnRemoveStateActionPerformed


