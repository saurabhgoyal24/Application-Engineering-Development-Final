    private void btnAddStateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddStateActionPerformed
        // TODO add your handling code here:
        String stateName = txtStateName.getText();
        
        Pattern pattern = Pattern.compile("[a-zA-Z ]+");
        Matcher statName = pattern.matcher(stateName);
        
        if(!statName.matches()){
            JOptionPane.showMessageDialog(null, "Invalid state name.");
            return;
        }
        CountryNetwork cn = (CountryNetwork)cmbCountry1.getSelectedItem();
        if(cn == null){
            JOptionPane.showMessageDialog(null,"No country is selected");
            return;
        }
        if(stateName.isEmpty()){
            JOptionPane.showMessageDialog(null,"Enter a state name.");
            return;
        }
        
        for (StateNetwork sn : cn .getStateNetwork()){
            if(sn.getStateName().equals(stateName)){
                JOptionPane.showMessageDialog(null, "This state name already exists.");
                return;
            }
        }
        StateNetwork sn = new StateNetwork();
        sn.setStateName(stateName);
        cn.addState(sn);
        
    }//GEN-LAST:event_btnAddStateActionPerformed

